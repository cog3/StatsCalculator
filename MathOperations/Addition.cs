using System;

namespace MathOperations
{
    public static class Addition
    {
        static public int Sum(int augEnd, int addEnd)
        {
            var sum = augEnd + addEnd;

            return sum;
        }
        static public double Sum(double augEnd, double addEnd)
        {
            var sum = augEnd + addEnd;

            return sum;
        }
    }
}
